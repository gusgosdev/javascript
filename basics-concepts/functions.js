// Creación y llamado de funciones

// Función de flecha que calculará el área de un triángulo
const miPrimeraFuncion = () => {
    // Área de un triángulo = (base * altura) / 2
    const base   = 20;
    const altura = 5;
    const area   = (base * altura) / 2;

    console.log(`El área de tu triángulo es: ${area}.`);
};

// Llamamos a la función
miPrimeraFuncion();