// Ciclos en JavaScript

// Ciclo while
console.log('----- Ciclo while -----');
let e = 0;
while(e < 10){
    console.log(`${e} aún es menor a 10`);
    e++;
}

// Ciclo for
console.log('----- Ciclo for -----');
for(let i = 0; i < 10; i++){
    console.log(`${i} aún es menor a 10`);
}

// Ciclo do while
console.log('----- Ciclo do while -----');
let u = 0;
do{
    console.log(`${u} aún es menor a 10`);
    u++;
} while(u < 10)