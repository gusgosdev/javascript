// Tipos de datos 

// String
let cadena  = 'Hola';
console.log(`La variable cadena es de tipo ${typeof cadena}.`);

// Number
let numero  = 10;
console.log(`La variable numero es de tipo ${typeof numero}.`);

// Boolean
let booleano = true;
console.log(`La variable booleano es de tipo ${typeof booleano}.`);

// Undefined
let indefinido;
console.log(`La variable indefinido es de tipo ${typeof indefinido}.`);

// Null
let nulo = null;
console.log(`La variable nulo es de tipo ${nulo}.`);

// Object
let objeto = {
    nombre: 'Lionel',
    apellido: 'Messi',
    edad: 37
}
console.log(`La variable objeto es de tipo ${typeof objeto}.`);